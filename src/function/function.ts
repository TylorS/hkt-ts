/**
 * A function which will always throw
 */
export function absurd<A>(): A {
  throw new Error(
    // eslint-disable-next-line prefer-rest-params
    `Absurd! You called with ${Array.from(arguments)
      .map((x) => JSON.stringify(x))
      .join()}`,
  )
}

